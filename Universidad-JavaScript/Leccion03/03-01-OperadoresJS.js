
let a = 3, b = 2;

let z = a + b;
console.log("Resultado de la suma: " + z);

z = a - b;
console.log("Resultado de la resta: " + z);

z = a * b;
console.log("Resultado de la multi: " + z);

z = a / b;
console.log("Resultado de la div: " + z);

z = a % b;
console.log("Resultado de la mod: " + z);

z = a ** b;
console.log("Resultado de la exponente: " + z);


//incremento
//pre-incremento (el operador ++ antes de la variable)
z = ++a;
console.log(a);
console.log(z);


//post-incremento (el operador ++ despues de la variable)

z = b++;
console.log(b);
console.log(z);

//Decremento
//pre-decremento
z = --a;
console.log(a);
console.log(z);

//Post-decremento
z = b--;
console.log(b);
console.log(z);




